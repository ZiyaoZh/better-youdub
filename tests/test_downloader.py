def test_download_url_to_artifacts_refuses_locked_task(tmp_path: Path) -> None:
    task_dir = tmp_path / "videos" / "Demo Author" / "20240601 Demo Video"
    task_dir.mkdir(parents=True)

    with TaskLock(task_dir, "existing-download"):
        with pytest.raises(TaskLockBusy):
            download_url_to_artifacts(
                "https://example.test/watch?v=demo123",
                tmp_path / "videos",
                DownloadConfig(youtube_dl_factory=FakeYoutubeDL),
            )

    assert FakeYoutubeDL.download_attempts == 0


def test_download_url_to_artifacts_retries_format_candidates(tmp_path: Path) -> None:
    FakeYoutubeDL.unavailable_download_attempts = 1

    result = download_url_to_artifacts(
        "https://example.test/watch?v=demo123",
        tmp_path / "videos",
        DownloadConfig(youtube_dl_factory=FakeYoutubeDL),
    )

    download_calls = [call for call in FakeYoutubeDL.calls if "format" in call]
    assert result.media_path.read_bytes() == b"video"
    assert [call["format"] for call in download_calls[:2]] == [
        "bestvideo[ext=mp4]+bestaudio[ext=m4a]/best[ext=mp4]/best",
        "bestvideo+bestaudio/best",
    ]
    assert all(call["format_sort"] == ["ext:mp4:m4a"] for call in download_calls[:2])


