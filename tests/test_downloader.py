def test_download_url_to_artifacts_uses_isolated_staging_dirs(tmp_path: Path) -> None:
    root = tmp_path / "videos"
    task_dir = root / "Demo Author" / "20240601 Demo Video"
    old_shared_staging = task_dir / ".download-staging"
    old_shared_staging.mkdir(parents=True)
    sentinel = old_shared_staging / "sentinel"
    sentinel.write_text("do not remove", encoding="utf-8")
    FakeYoutubeDL.preserved_staging_path = sentinel
    FakeYoutubeDL.unavailable_download_attempts = 1

    download_url_to_artifacts(
        "https://example.test/watch?v=demo123",
        root,
        DownloadConfig(youtube_dl_factory=FakeYoutubeDL),
    )

    download_calls = [call for call in FakeYoutubeDL.calls if "outtmpl" in call]
    staging_dirs = {Path(call["outtmpl"]).parent.name for call in download_calls}
    assert len(staging_dirs) == 2
    assert all(name.startswith(".download-staging-") for name in staging_dirs)
    assert old_shared_staging.exists()
    assert sentinel.read_text(encoding="utf-8") == "do not remove"
    assert not list(task_dir.glob(".download-staging-*"))


def test_download_url_to_artifacts_force_enables_overwrite(tmp_path: Path) -> None:
    download_url_to_artifacts(
        "https://example.test/watch?v=demo123",
        tmp_path / "videos",
        DownloadConfig(force=True, youtube_dl_factory=FakeYoutubeDL),
    )

    download_call = next(call for call in FakeYoutubeDL.calls if "format" in call)
    assert download_call["overwrites"] is True


def test_download_url_to_artifacts_does_not_let_ytdlp_mutate_original_cookies(tmp_path: Path) -> None:
    original_cookie_text = (
        "# Netscape HTTP Cookie File\n"
        ".youtube.com\tTRUE\t/\tTRUE\t1815872581\tLOGIN_INFO\tsecret\n"
    )
    cookies = tmp_path / "cookies.txt"
    cookies.write_text(original_cookie_text, encoding="utf-8")
    FakeYoutubeDL.mutate_cookiefile = True

    download_url_to_artifacts(
        "https://example.test/watch?v=demo123",
        tmp_path / "videos",
        DownloadConfig(cookies_path=cookies, youtube_dl_factory=FakeYoutubeDL),
    )

    assert cookies.read_text(encoding="utf-8") == original_cookie_text
    cookiefiles = {call["cookiefile"] for call in FakeYoutubeDL.calls if "cookiefile" in call}
    assert cookiefiles
    assert str(cookies.resolve()) not in cookiefiles
    assert all(not Path(cookiefile).exists() for cookiefile in cookiefiles)
