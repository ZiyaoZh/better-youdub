from youdub.locking import TaskLock, TaskLockBusy


class FakeYoutubeDL:
    calls: list[dict[str, Any]] = []
    download_attempts = 0
    unavailable_download_attempts = 0
    mutate_cookiefile = False
    staging_parent_path: Path | None = None
    preserved_staging_path: Path | None = None

    def __init__(self, params: dict[str, Any]):
        self.params = params
        FakeYoutubeDL.calls.append(params)

    def __enter__(self) -> "FakeYoutubeDL":
        return self

    def __exit__(self, *_: object) -> None:
        return None

    def extract_info(self, url: str, download: bool = False) -> dict[str, Any]:
        assert download is False
        self._mutate_cookiefile()
        return {
            "extractor_key": "Youtube",
            "id": "demo123",
            "title": "Demo Video",
            "uploader": "Demo Author",
            "upload_date": "20240601",
            "webpage_url": url,
        }

    def sanitize_info(self, info: dict[str, Any]) -> dict[str, Any]:
        return dict(info)

    def download(self, urls: list[str]) -> None:
        assert urls == ["https://example.test/watch?v=demo123"]
        self._mutate_cookiefile()
        FakeYoutubeDL.download_attempts += 1
        if FakeYoutubeDL.download_attempts <= FakeYoutubeDL.unavailable_download_attempts:
            raise RuntimeError("Requested format is not available")
        output_template = Path(self.params["outtmpl"])
        output_template.parent.mkdir(parents=True, exist_ok=True)
        if FakeYoutubeDL.staging_parent_path is None:
            FakeYoutubeDL.staging_parent_path = output_template.parent.parent
        assert output_template.parent.parent == FakeYoutubeDL.staging_parent_path
        assert output_template.parent.name.startswith(".download-staging-")
        assert output_template.parent.name != ".download-staging"
        if FakeYoutubeDL.preserved_staging_path is not None:
            assert FakeYoutubeDL.preserved_staging_path.exists()
        output_template.with_suffix(".mp4").write_bytes(b"video")
        output_template.with_suffix(".webp").write_bytes(b"cover")

    def _mutate_cookiefile(self) -> None:
        if not FakeYoutubeDL.mutate_cookiefile:
            return
        cookiefile = self.params.get("cookiefile")
        if cookiefile:
            Path(cookiefile).write_text("# This file is generated by yt-dlp.\n", encoding="utf-8")


