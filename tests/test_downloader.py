def test_ytdlp_base_options_uses_nonempty_cookies_and_proxy(tmp_path: Path) -> None:
    supported_js_runtimes.cache_clear()
    cookies = tmp_path / "cookies.txt"
    cookies.write_text("# Netscape HTTP Cookie File\n", encoding="utf-8")

    options = ytdlp_base_options(DownloadConfig(cookies_path=cookies, proxy="http://127.0.0.1:7890"))

    assert options["cookiefile"] == str(cookies.resolve())
    assert options["proxy"] == "http://127.0.0.1:7890"
    assert options["remote_components"] == ["ejs:github"]
    assert options["extractor_args"] == {"youtube": {"player_js_variant": ["main"]}}


def test_ytdlp_base_options_ignores_empty_cookies(tmp_path: Path) -> None:
    cookies = tmp_path / "cookies.txt"
    cookies.write_text("", encoding="utf-8")

    options = ytdlp_base_options(DownloadConfig(cookies_path=cookies))

    assert "cookiefile" not in options


def test_supported_js_runtimes_prefers_supported_deno_and_ignores_old_node(monkeypatch) -> None:
    import youdub.downloader as downloader

    supported_js_runtimes.cache_clear()
    monkeypatch.setattr(downloader.shutil, "which", lambda name: f"/usr/bin/{name}" if name in {"deno", "node"} else None)

    def fake_run(args: list[str], **_: Any) -> subprocess.CompletedProcess[str]:
        output = {"deno": "deno 2.5.6\n", "node": "v20.20.2\n"}[Path(args[0]).name]
        return subprocess.CompletedProcess(args, 0, stdout=output, stderr="")

    monkeypatch.setattr(downloader.subprocess, "run", fake_run)

    assert supported_js_runtimes() == {"deno": {"path": "/usr/bin/deno"}}


def test_supported_js_runtimes_accepts_node_22(monkeypatch) -> None:
    import youdub.downloader as downloader

    supported_js_runtimes.cache_clear()
    monkeypatch.setattr(downloader.shutil, "which", lambda name: "/usr/local/bin/node" if name == "node" else None)
    monkeypatch.setattr(
        downloader.subprocess,
        "run",
        lambda args, **_: subprocess.CompletedProcess(args, 0, stdout="v22.16.0\n", stderr=""),
    )

    assert supported_js_runtimes() == {"node": {"path": "/usr/local/bin/node"}}


def test_format_candidates_uses_requested_height() -> None:
    assert format_candidates(720)[0] == (
        "bestvideo[ext=mp4][height<=720]+bestaudio[ext=m4a]/"
        "best[ext=mp4][height<=720]/best[height<=720]/best"
    )
    assert format_candidates(720)[-1] is None
    assert format_candidates(0)[0] == (
        "bestvideo[ext=mp4]+bestaudio[ext=m4a]/best[ext=mp4]/best"
    )
    assert all(candidate is None or "height<=" not in candidate for candidate in format_candidates(0))
    assert format_sort(0) == ["ext:mp4:m4a"]
    assert format_sort(720) == ["res:720", "ext:mp4:m4a"]


def test_download_url_to_artifacts_writes_expected_files(tmp_path: Path) -> None:
    result = download_url_to_artifacts(
        "https://example.test/watch?v=demo123",
        tmp_path / "videos",
        DownloadConfig(youtube_dl_factory=FakeYoutubeDL),
    )

    assert result.media_path.name == "download.mp4"
    assert result.media_path.read_bytes() == b"video"
    assert result.cover_path is not None
    assert result.cover_path.name == "download.webp"
    assert result.source_key == "youtube:demo123"
    assert json.loads(result.info_path.read_text(encoding="utf-8"))["title"] == "Demo Video"
    assert result.task_dir == tmp_path / "videos" / "Demo Author" / "20240601 Demo Video"


